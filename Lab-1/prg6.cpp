// Write a program to input two numbers from the keyboard and calculate multiplication and then
// print the result
#include <iostream>
using namespace std;
int main(){
    int a,b;
    cout << "Enter the value of a :- ";
    cin >> a;
    cout << "Enter the value of b :- ";
    cin >> b;
    cout << "The multiplication of " << a <<" and " << b << " = " << a*b << "\n";

}